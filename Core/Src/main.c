//コマンド送信用関数
void writeCmd(uint8_t cmd)
{
	uint8_t buf[] = {CMDSEND,cmd};
	HAL_I2C_Master_Transmit(&hi2c1,LCDADRESS,buf,sizeof(buf),HAL_MAX_DELAY);
}

//データ送信用関数
void writeData(uint8_t data)
{
	uint8_t buf[] = {DATASEND,data};
	HAL_I2C_Master_Transmit(&hi2c1 ,LCDADRESS , buf,sizeof(buf),HAL_MAX_DELAY);
}

//LCD初期化関数
void Lcd_init(){
	writeCmd(0x01);
	HAL_Delay(5);		//200msまち
	writeCmd(0x38);		//ファンクション設定
	HAL_Delay(5);		//200ms
	writeCmd(0x0f);		//ディスプレイＯＮ、CURSOR-ON、blinking-ON
	HAL_Delay(5);		//200msまち
	writeCmd(0x06);		//データ書き込み後アドレス加算モード設定
	HAL_Delay(5);		//200msまち
}

//LCD画面クリア
void LCD_Clear(void){
	writeCmd(0x01);
	HAL_Delay(2);
}

//LCDのカーソル位置設定
void LCD_SetCursor(uint8_t col,uint8_t row)
{
	uint8_t row_offsets[] = { 0x00, 0x40, 0x14, 0x54 };
	if (row > 1) {
	row = 1;
	}
	writeCmd(0x80 | (col + row_offsets[row]));
}

//LCD文字出力
void LCD_Print(char* str)
{
	while (*str) {
		writeData((uint8_t)*str++);
	}
}
